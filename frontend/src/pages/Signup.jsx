import React, { useState } from 'react'

import { Button } from "@/components/ui/button"
import {
  Card,
  CardAction,
  CardContent,
  CardDescription,
  CardFooter,
  CardHeader,
  CardTitle,
} from "@/components/ui/card"
import { Input } from "@/components/ui/input"
import { Label } from "@/components/ui/label"
import { Eye, EyeOff } from 'lucide-react'
import { Link, useNavigate } from 'react-router-dom'
import { toast } from 'sonner'
import axios from 'axios'




const Signup = () => {

  const [showPassword , setShowPassword]=useState(false)
  const[loading,setLoading] = useState(false)
  const[formData, setFormData]= useState({
    firstName:'',
    lastName:'',
    email:'',
    password:''
  })

  const navigate = useNavigate()

  const handleChange =(e)=>{
    const {name ,value} = e.target;
    setFormData((prev)=>({
      ...prev,
      [name]:value
    }))
  }

  const submitHandler =  async(e) =>{
      e.preventDefault()
      console.log(formData)
     try{
      const res = await axios.post(`http://127.0.0.1:8000/api/v1/user/register
      `,formData ,{
      headers: {
        "Content-type":"application/json"
      }  
      })

      if(res.data.success){
        navigate('/verify')
        toast.success(res.data.message)
      }
     }catch(error){
         console.log(error)
         toast.error(error.response?.data?.message || "Something went wrong")

     }
  }

  return (
    <div className='flex justify-center items-center min-h-screen bg-pink-100'>
        <Card className="w-full max-w-sm">
      <CardHeader>
        <CardTitle>Create your account</CardTitle>
        <CardDescription>
          Enter given details below to create your account
        </CardDescription>
      </CardHeader>
      <CardContent>
        
          <div className="flex flex-col gap-3">
            <div className="grid grid-cols-2 gap-4">
            <div className='grid gap-2'>
            <Label htmlFor = "firstName"> FirstName</Label>
            <input
              id= "firstName"
              name='firstName'
              type='text'
              placeholder='john'
              required
              value={formData.firstName}
              onChange={handleChange}
            />

            </div>

            <div className='grid gap-2'>
            <Label htmlFor = "lastName"> lastName</Label>
            <input
              id= "lastName"
              name='lastName'
              type='text'
              placeholder='Doe'
              required
              value={formData.lastName}
              onChange={handleChange}
            />

            </div>
              </div>

              <div className='grid gap-2'>
              <Label htmlFor="email">Email</Label>
              <Input
                id="email"
                name= "email"
                type="email"
                placeholder="m@example.com"
                required
                value={formData.email}
              onChange={handleChange}
              />
              </div>
            
            <div className="grid gap-2">
              <div className="flex items-center">
                <Label htmlFor="password">Password</Label>
              </div>
              <div className='relative'>
              <Input 
              id="password"
              name="password" 
              placeholder="create Paswword"
              type= {showPassword ? 'text': 'password'}
              required
              value={formData.password}
              onChange={handleChange}
               />
              {
                showPassword ? <EyeOff onClick={()=>setShowPassword(false)} className='w-5 h-5 text-gray-700 absolute right-5 bottom-2'/>  : 

                <Eye onClick={()=>setShowPassword(true)} className='w-5 h-5 text-gray-700 absolute right-5 bottom-2'/> 
              }
              </div>
              
            </div>
          </div>
        
      </CardContent>
      <CardFooter className="flex-col gap-2">
        <Button type="submit" className="w-full cursor-pointer bg-pink-600 hover:bg-pink-500" onClick = {submitHandler} >
          SignUp
        </Button>
        <p className='text-gray-700 text-sm'>Already have an account ? <Link to ={'/login'} className='hover:underline cursor-pointer text-pink-800 hover:text-pink-500'>Login</Link></p>
        {/* <Button variant="outline" className="w-full">
          Login with Google
        </Button> */}
      </CardFooter>
    </Card>
    </div>
  )
}

export default Signup
